# set system timezone
#	$1 	- the timezone name
#	$2	- the timezone string
setTimezone () {
	local timezone="$1"
	local tz="$2"

	uci set system.@system[0].timezone="$tz"
	uci set system.@system[0].zonename="$timezone"
	uci commit system

	echo "$tz" > /etc/TZ
	echo "$tz" > /tmp/TZ
}

# sync time with online NTP servers
syncTime () {
	/etc/init.d/sysntpd restart
}

